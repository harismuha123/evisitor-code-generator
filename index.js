document.getElementById("searchterm").addEventListener("keypress", function (event) {
    if (event.key === 'Enter') {
        getResult();
    }
});


function getResult() {
    var input = document.getElementById("searchterm").value;
    var result = search(input);
    document.getElementById("resulttext").innerHTML = result;
}

